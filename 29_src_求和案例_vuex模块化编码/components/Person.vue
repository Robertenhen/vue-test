<template>
  <div>
    <h1>人员列表</h1>
    <input
      type="text"
      placeholder="请输入名字"
      v-model="name"
      @keyup.enter="add"
    />
    <h3 style="color: green">Count组件的求和为：{{ sum }}</h3>
    <h3>列表中第一个人的名字为：{{ firstPersonName }}</h3>
    <button @click="add">添加</button>
    <button @click="addWang">添加一个姓王的人</button>
    <button @click="addPersonServer">添加一个人，名字随机</button>
    <ul>
      <li v-for="person in personList" :key="person.id">
        {{ person.name }}
      </li>
    </ul>
  </div>
</template>

<script>
import { nanoid } from "nanoid";
export default {
  name: "MyPerson",
  data() {
    return {
      name: "",
    };
  },
  mounted() {
    // console.log('???',this.$store);
  },
  computed: {
    personList() {
      return this.$store.state.personAbout.personList;
    },
    sum() {
      return this.$store.state.countAbout.total;
    },
    firstPersonName() {
      return this.$store.getters["personAbout/firstPersonName"];
    },
  },
  methods: {
    add() {
      const p = {
        id: nanoid(),
        name: this.name,
      };
      this.$store.commit("personAbout/ADD_PERSON", p);
      this.name = "";
    },
addWang() {
  const p = {
        id: nanoid(),
        name: this.name,
      };
      this.$store.dispatch("personAbout/addPersonWang", p);
      this.name = "";
    },
    addPersonServer() { 
      this.$store.dispatch("personAbout/addPersonServer");
    }
  },
};
</script>

